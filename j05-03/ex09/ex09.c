#include <unistd.h>
#include <stdio.h>

char	*ft_strlowcase(char *str)
{
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
		}
		str++;
	}
	return (str);
}

int		main(void)
{
	char str[] = "HELLO";

	ft_strlowcase(str);
	printf("%s\n", str);
}
