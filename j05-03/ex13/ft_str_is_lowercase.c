int		ft_str_is_lowercase(char *str)
{
	int i;

	i = 1;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			i = i * 1;
		}
		else
			i = 0;
		str++;
	}
	return (i);
}
