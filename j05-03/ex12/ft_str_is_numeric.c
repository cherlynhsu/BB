int		ft_str_is_numeric(char *str)
{
	int	i;

	i = 1;
	while (*str != '\0')
	{
		if (*str >= '0' && *str <= '9')
		{
			i = i * 1;
		}
		else
			i = 0;
		str++;
	}
	return (i);
}
