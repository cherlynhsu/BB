int		ft_atoi(char *str)
{
	int i;
	int sign;
	int o;

	sign = 0;
	i = 0;
	o = 0;
	while ((str[i] == '\n' || str[i] == '\f'
		|| str[i] == '\v' || str[i] == '\t' || str[i] == '\r' || str[i] == ' '))
		i++;
	if (str[i] == '-')
		sign = 1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while ((str[i] != '\0') && (str[i] >= '0') && (str[i] <= '9'))
	{
		o = 10 * o + str[i] - '0';
		i++;
	}
	return (sign == 1 ? o * (-1) : o);
}
